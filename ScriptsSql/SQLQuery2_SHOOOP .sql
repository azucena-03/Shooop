ALTER PROCEDURE spProductsCategory 
@CAT_ID INT
AS
BEGIN
	SELECT PRO_NAME,
	       PRO_IMG,
		   PRICE
	FROM [dbo].[PRODUCT]
	WHERE @CAT_ID = CAT_ID AND
	PRICE - DESCOUNT > 0
END
GO
