ALTER PROCEDURE spProductsName 
@PRO_NAME VARCHAR(50)
AS
BEGIN
	SELECT PRO_NAME,
	       PRO_IMG,
		   PRICE
	FROM [dbo].[PRODUCT]
	WHERE @PRO_NAME = PRO_NAME AND
	PRICE - DESCOUNT > 0
END
GO

